#include <strings.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (i < n)
	{
		*str = (char)c;
		str++;
		i++;
	}
	return (s);
}
