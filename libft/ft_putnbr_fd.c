#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	long int	n;
	char		num;

	n = nb;
	num = '0';
	if (n < 0)
	{
		n = n * -1;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		num = num + n;
		write(fd, &num, 1);
	}
}
